const isValidSudoku = function (board) {

    let rowNums = []
    let columnNums = []
    let squareNums = []

    for (let row of board) {
        for (let num of row) {
            if (!rowNums.includes(num)) {
                if (num > 0 && num < 10) rowNums.push(num)
            }
            else return false
        }
        rowNums = []
    }

    for (let i = 0; i < 9; i++) {
        for (let row of board) {
            if (!columnNums.includes(row[i])) {
                if (row[i] > 0 && row[i] < 10) columnNums.push(row[i])
            }
            else return false
        }

        columnNums = []
    }

    let col = 0
    let row = 0

    while (row < 7) {
        while (col < 7) {
            for (let i = row; i < row + 3; i++) {
                for (let j = col; j < col + 3; j++) {
                    if (!squareNums.includes(board[i][j])){
                        if (board[i][j] > 0 && board[i][j] < 10) squareNums.push(board[i][j])
                    }
                    else return false
                }
            }
            squareNums = []
            col += 3
        }
        col = 0
        row += 3
    }


    return true

};


